package Estruturas_de_repeticao;

public class while_1 {

    public static void main(String[] args){

        // Declarando a variável do contador
        int contador = 0;
        // Declarando a condição da estrutura de repetição whilee
        while(contador < 10){

            // Mensagem a ser exibida no terminal
            System.out.println("Hello World");
            // Incrementando o contador para impor o limite para a estrutura de repetição while
            contador++;

        }

    }

}
